package com.warkiz.widget;

public interface TickMarkType {
    /**
     * Tick mark is GONE
     */
    int NONE = 0;
    /**
     * Tick mark shape as regular oval
     */
    int OVAL = 1;
    /**
     * Tick mark shape as regular square
     */
    int SQUARE = 2;
    /**
     * Tick mark shape as vertical line (2dp size)
     */
    int DIVIDER = 3;
}